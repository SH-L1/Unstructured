create table spatial_location_resolution_runs (
    id uuid not null,
    complaint_id uuid,
    complaint_issue_id uuid,
    input_text text not null,
    normalized_location_text text,
    status varchar(40) not null,
    blocker varchar(40),
    created_at timestamp not null,
    updated_at timestamp not null,
    constraint pk_spatial_location_resolution_runs primary key (id),
    constraint fk_spatial_location_runs_complaint foreign key (complaint_id) references complaints (id),
    constraint fk_spatial_location_runs_issue foreign key (complaint_issue_id) references complaint_issues (id)
);

create table spatial_location_candidates (
    id uuid not null,
    run_id uuid not null,
    candidate_type varchar(60) not null,
    label varchar(300) not null,
    address text,
    source_layer varchar(80) not null,
    source_record_id varchar(120),
    admin_boundary_id bigint,
    confidence varchar(20) not null,
    confidence_score decimal(5,4) not null,
    needs_human_confirmation boolean not null default true,
    latitude decimal(12,8),
    longitude decimal(12,8),
    evidence_summary text,
    created_at timestamp not null,
    constraint pk_spatial_location_candidates primary key (id),
    constraint fk_spatial_location_candidates_run foreign key (run_id) references spatial_location_resolution_runs (id),
    constraint fk_spatial_location_candidates_boundary foreign key (admin_boundary_id) references spatial_admin_boundaries (id)
);

