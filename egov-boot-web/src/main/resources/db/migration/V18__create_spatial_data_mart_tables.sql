create table spatial_source_registry (
    id bigint generated by default as identity,
    source_name varchar(200) not null,
    source_type varchar(60) not null,
    phase integer not null,
    provider varchar(120),
    source_url varchar(500),
    jurisdiction_code varchar(80) default 'ASAN',
    license_note text,
    data_standard varchar(120),
    collected_at timestamp,
    data_effective_on date,
    content_hash varchar(128),
    status varchar(40) not null default 'READY',
    created_at timestamp not null,
    updated_at timestamp not null,
    constraint pk_spatial_source_registry primary key (id),
    constraint uk_spatial_source_registry_name unique (source_name)
);

create table spatial_admin_boundaries (
    id bigint generated by default as identity,
    source_id bigint not null,
    boundary_type varchar(40) not null,
    boundary_code varchar(80),
    boundary_name varchar(200) not null,
    parent_code varchar(80),
    valid_from date,
    valid_to date,
    centroid_lat decimal(12,8),
    centroid_lon decimal(12,8),
    min_lat decimal(12,8),
    max_lat decimal(12,8),
    min_lon decimal(12,8),
    max_lon decimal(12,8),
    geometry_format varchar(20),
    geometry_payload text,
    geometry_quality varchar(40) not null default 'SOURCE_PROVIDED',
    created_at timestamp not null,
    updated_at timestamp not null,
    constraint pk_spatial_admin_boundaries primary key (id),
    constraint fk_spatial_admin_boundaries_source foreign key (source_id) references spatial_source_registry (id),
    constraint uk_spatial_admin_boundaries unique (source_id, boundary_type, boundary_code, boundary_name)
);

