create index idx_spatial_admin_boundaries_bbox on spatial_admin_boundaries (min_lat, max_lat, min_lon, max_lon);
create index idx_spatial_address_points_admin on spatial_address_points (admin_name, legal_dong_name);
create index idx_spatial_address_points_lat_lon on spatial_address_points (latitude, longitude);
create index idx_spatial_road_segments_name on spatial_road_segments (road_name);
create index idx_spatial_facilities_type_name on spatial_facilities (facility_type, facility_name);
create index idx_spatial_facilities_admin on spatial_facilities (admin_name);
create index idx_spatial_facilities_lat_lon on spatial_facilities (latitude, longitude);
create index idx_spatial_parking_road on spatial_parking_restrictions (road_name, restriction_type);
create index idx_spatial_location_runs_complaint on spatial_location_resolution_runs (complaint_id, complaint_issue_id, created_at);
create index idx_spatial_location_candidates_run on spatial_location_candidates (run_id, confidence_score);
